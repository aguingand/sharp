<template>
    <div>
        <LocalizedEditors
            :editor="editor"
            :name="uniqueIdentifier"
            :value="value"
            :locale="locale"
            :locales="locales"
            :create-editor="createEditor"
            v-slot="{ editor, locale }"
        >
            <SharpEditor
                :editor="editor"
                :toolbar-options="toolbarOptions(editor)"
                v-bind="$props"
                @update="handleUpdate({ editor, locale, ...$event })"
            />
        </LocalizedEditors>
    </div>
</template>

<script>
    import { Markdown } from 'tiptap-markdown';
    import { Editor } from '@tiptap/vue-3';
    import { __ } from "@/utils/i18n";
    import SharpEditor from '../../Editor.vue';
    import { defaultEditorOptions, editorProps } from "../../index";
    import { LocalizedEditor } from '../../../../../mixins/localize/editor';
    import LocalizedEditors from "../../LocalizedEditors.vue";
    import { normalizeText } from "../../../../../util/text";
    import { config } from "@/utils/config";


    export default {
        mixins: [
            LocalizedEditor
        ],
        components: {
            SharpEditor,
            LocalizedEditors,
        },
        props: {
            ...editorProps,
            extensions: Array,
            toolbar: Array,

            readOnly: Boolean,
            uniqueIdentifier: String,
        },
        data() {
            return {
                editor: null,
            }
        },
        methods: {
            handleUpdate({ editor, locale, error }) {
                const content = normalizeText(editor.storage.markdown.getMarkdown() ?? '');
                this.$emit('input', this.localizedValue(content, locale), { error });
            },
            toolbarOptions(editor) {
                const options = [];
                const hasList = this.toolbar?.some(button => button === 'bullet-list' || button === 'ordered-list');

                if(!config('sharp.markdown_editor.tight_lists_only') && hasList) {
                    options.push({
                        command: () => editor.chain().toggleTight().run(),
                        disabled: !editor.can().toggleTight(),
                        label: __('sharp::form.editor.dropdown.options.toggle_tight_list'),
                    });
                }

                return options;
            },

            createEditor({ content }) {
                return new Editor({
                    ...defaultEditorOptions,
                    extensions: [
                        ...this.extensions,
                        Markdown.configure({
                            breaks: config('sharp.markdown_editor.nl2br'),
                        }),
                    ],
                    content,
                    editable: !this.readOnly,
                });
            },
        },
        created() {
            if(!this.isLocalized) {
                this.editor = this.createEditor({
                    content: this.localizedText,
                });
            }
        },
        beforeDestroy() {
            this.editor?.destroy();
        },
    }
</script>
